'''
列表引用与拷贝：对比 l2 = l1 和 l2 = l1.copy()
已知接口速率列表:

l1 = [100, 1000, 10, 400, 25, 40, 0]
将 l1 赋值给 l2（可以尝试引用 l2 = l1 或拷贝 l2 = l1.copy()），然后对 l2 进行从小到大排序，最后用循环并排打印 l1 和 l2:

l1      l2
100     0
1000    10
10      25
400     40
25      100
40      400
0       1000
'''

l1 = [100, 1000, 10, 400, 25, 40, 0]
l2 = l1.copy()

# 从小到大排序
l2.sort()

print("l1      l2")

# 循环并排打印 l1 和 l2
for a, b in zip(l1, l2):
    print(f"{a:<8}{b}")


#思考：l2复制了l1的表就是一个独立的表了